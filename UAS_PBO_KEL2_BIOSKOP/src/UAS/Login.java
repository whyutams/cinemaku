    private void btn_loginbtn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginbtn_loginActionPerformed
        // TODO add your handling code here:
        if (tusername.getText().length() == 0 || tpassword.getText().length() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Username dan Password harus diisi!",
                    "Login Gagal",
                    JOptionPane.ERROR_MESSAGE
            );

            return;
        }

        String username = tusername.getText();
        String password = tpassword.getText();

        try {
            ResultSet rs = func.db.select("select * from akun where username='" + username + "' and password='" + password + "'");

            if (rs.next()) {
                Home home = new Home(rs.getString("id_akun"));
                this.dispose();
                home.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null,
                        "Username atau password salah.",
                        "Login Gagal",
                        JOptionPane.ERROR_MESSAGE
                );
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null,
                    "Terjadi kesalahan: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btn_loginbtn_loginActionPerformed

